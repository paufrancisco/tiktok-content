public class  swapnumbers_using_third_value {
    public static void main(String[] args) {

        

        int number1 = 15;
        int number2 = 0; 

        int temporary_container;

        temporary_container = number1;
        number1 = number2;
        number2 = temporary_container;


        System.out.println("This is number 1: "+ number1);
        System.out.println("This is number 2: "+ number2);

        


    }
}